import type { CorpusItem, RomajiProfileId } from "../types";
import { ROMAJI_PROFILES, normalizeReading, toRomajiUnits } from "../engine/romanizer";

type CorpusSeed = Omit<CorpusItem, "romaji"> & Partial<Pick<CorpusItem, "romaji">>;

function attachRomaji(items: CorpusSeed[], profileId: RomajiProfileId = "jis"): CorpusItem[] {
  const profile = ROMAJI_PROFILES[profileId];
  return items.map((item) => ({
    ...item,
    romaji: item.romaji ?? toRomajiUnits(normalizeReading(item.reading), profile).join(""),
  }));
}

