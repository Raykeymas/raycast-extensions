export const SENTENCE_CORPUS: CorpusItem[] = attachRomaji(SENTENCE_CORPUS_SEED);

export function getRandomItem(difficulty: 1 | 2 | 3, excludeIds: Set<string> = new Set()): CorpusItem | null {
  const candidates = DEFAULT_CORPUS.filter((item) => item.difficulty === difficulty && !excludeIds.has(item.id));
  if (candidates.length === 0) return null;
  return candidates[Math.floor(Math.random() * candidates.length)];
}

export function getSentenceItem(difficulty: 1 | 2 | 3, excludeIds: Set<string> = new Set()): CorpusItem | null {
  const candidates = SENTENCE_CORPUS.filter(
    (item) => item.difficulty === difficulty && item.mode === "sentence" && !excludeIds.has(item.id),
  );
  if (candidates.length === 0) return null;
  return candidates[Math.floor(Math.random() * candidates.length)];
}

export function getCorpusByDifficulty(difficulty: 1 | 2 | 3): CorpusItem[] {
  return DEFAULT_CORPUS.filter((item) => item.difficulty === difficulty);
}

export function getCorpusByMode(mode: "word" | "sentence", difficulty: 1 | 2 | 3): CorpusItem[] {
  const corpus = mode === "word" ? DEFAULT_CORPUS : SENTENCE_CORPUS;
  return corpus.filter((item) => item.difficulty === difficulty && item.mode === mode);
}
